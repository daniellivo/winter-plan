// Parses the 3 Teknon hospitalization CSVs into a typed dataset
// committed at src/data/teknonShifts.ts.
//
// CSV layout (per specialty file):
//   - First block under "Ejemplo,," is an illustrative example — skipped.
//   - "JUNIO"/"JULIO"/"AGOSTO"/"SEPTIEMBRE" rows mark the month being read.
//   - Each weekly block has:
//       row A:  ",,Lunes,Martes,Miercoles,Jueves,Viernes,Sabado,Domingo"
//       row B:  ",,1-Jun,2-Jun,...,7-Jun"
//       row C:  "07:10-14:10,Mañanas,<count or empty per day>"
//       row D:  "14:00-21:00,Tardes,<...>"
//       row E:  "20:45-07:30,Noches,<...>"
//   - Empty rows separate blocks.

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')

const SPECIALTIES = ['adultos', 'pediatria', 'materno']
const YEAR = 2026
const MONTH_NUMBER = {
  Jun: 6, Jul: 7, Aug: 8, Ago: 8, Sep: 9,
}

function parseCsv(text) {
  // Naive CSV split — no quoted commas in these files.
  return text
    .replace(/\r/g, '')
    .split('\n')
    .map(line => line.split(','))
}

function parseDateCell(cell) {
  // Cells look like "3-Jun" / "31-Aug" / "1-Sep".
  const trimmed = cell.trim()
  if (!trimmed) return null
  const match = /^(\d{1,2})-(Jun|Jul|Aug|Ago|Sep)$/.exec(trimmed)
  if (!match) return null
  const day = Number(match[1])
  const month = MONTH_NUMBER[match[2]]
  if (!month) return null
  return `${YEAR}-${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`
}

const SLOT_BY_KEYWORD = {
  mañanas: 'TM',
  mananas: 'TM',
  tardes: 'TT',
  noches: 'TN',
}

function buildDataset(rows) {
  const result = []
  // Walk rows, skipping the initial "Ejemplo" block (rows before the first MONTH header).
  let seenFirstMonthHeader = false

  for (let i = 0; i < rows.length; i++) {
    const row = rows[i]
    const firstCell = (row[0] || '').trim()

    // Detect month header rows like "JUNIO,,,,," — they always appear in col A.
    if (/^(JUNIO|JULIO|AGOSTO|SEPTIEMBRE)$/i.test(firstCell)) {
      seenFirstMonthHeader = true
      continue
    }

    if (!seenFirstMonthHeader) continue

    // Date row: col A empty, col B empty, cols C..I have "<n>-<Mon>".
    const isDateRow =
      firstCell === '' &&
      (row[1] || '').trim() === '' &&
      row.slice(2, 9).some(c => /-(Jun|Jul|Aug|Ago|Sep)$/.test((c || '').trim()))

    if (!isDateRow) continue

    const dates = row.slice(2, 9).map(parseDateCell)

    // Next 3 rows = TM, TT, TN counts.
    for (let k = 1; k <= 3; k++) {
      const slotRow = rows[i + k]
      if (!slotRow) break
      const slotKey = (slotRow[1] || '').trim().toLowerCase()
      const slot = SLOT_BY_KEYWORD[slotKey]
      if (!slot) continue
      slotRow.slice(2, 9).forEach((cell, idx) => {
        const date = dates[idx]
        if (!date) return
        const raw = (cell || '').trim()
        if (!raw) return
        const count = Number(raw)
        if (!Number.isFinite(count) || count <= 0) return
        result.push({ date, slot, count })
      })
    }
  }

  // Dedup: a date can appear in multiple blocks (e.g. overlapping weeks). Keep max count.
  const dedup = new Map()
  for (const entry of result) {
    const key = `${entry.date}|${entry.slot}`
    const prev = dedup.get(key)
    if (!prev || entry.count > prev.count) dedup.set(key, entry)
  }

  return [...dedup.values()].sort((a, b) => {
    if (a.date !== b.date) return a.date < b.date ? -1 : 1
    const order = { TM: 0, TT: 1, TN: 2 }
    return order[a.slot] - order[b.slot]
  })
}

const datasets = {}
for (const specialty of SPECIALTIES) {
  const csvPath = resolve(ROOT, 'src/data/csv', `${specialty}.csv`)
  const rows = parseCsv(readFileSync(csvPath, 'utf-8'))
  datasets[specialty] = buildDataset(rows)
}

const header = `// Auto-generated by scripts/build-teknon-data.mjs.
// Do not edit by hand — re-run \`node scripts/build-teknon-data.mjs\` after updating CSVs.

export type TeknonSpecialty = 'adultos' | 'pediatria' | 'materno'
export type TeknonSlot = 'TM' | 'TT' | 'TN'

export interface TeknonShift {
  date: string // YYYY-MM-DD
  slot: TeknonSlot
  count: number
}

export const TEKNON_SHIFTS: Record<TeknonSpecialty, TeknonShift[]> = `

const body = JSON.stringify(datasets, null, 2)
  // Tighten the array of objects to one entry per line for readability.
  .replace(/\{\n\s+"date": ("[^"]+"),\n\s+"slot": ("[^"]+"),\n\s+"count": (\d+)\n\s+\}/g,
    '{ date: $1, slot: $2, count: $3 }')
  .replace(/"date":/g, 'date:')
  .replace(/"slot":/g, 'slot:')
  .replace(/"count":/g, 'count:')
  .replace(/"adultos":/g, 'adultos:')
  .replace(/"pediatria":/g, 'pediatria:')
  .replace(/"materno":/g, 'materno:')

writeFileSync(resolve(ROOT, 'src/data/teknonShifts.ts'), header + body + '\n')

console.log('✅ Wrote src/data/teknonShifts.ts')
for (const specialty of SPECIALTIES) {
  console.log(`   ${specialty}: ${datasets[specialty].length} slots`)
}
